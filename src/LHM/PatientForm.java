    private LHM LHMAppointment;
    
    public void initWarehouse() {
        //System.setSecurityManager(new RMISecurityManager());
        try {
            String url = "rmi://localhost/LHM-Appointment";
            LHMAppointment = (LHM) Naming.lookup(url);
        } catch (Exception e) {
            System.out.println("Error: Can't connect to LHM-Appointment. " + e);
        }
    }
    
    
    
    private void callWarehouse(Patient c) {
        try {
            Vector recommendations = LHMAppointment.find(c);
            ResponseBox.setText(c + "\n");
            for (int i = 0; i < recommendations.size(); i++) {
                History p = (History) recommendations.get(i);
                String t = p.getDescription() + "\n";
                ResponseBox.append(t);
            }
        } catch (Exception e) {
            ResponseBox.setText("Error: " + e);
        }
    }    
