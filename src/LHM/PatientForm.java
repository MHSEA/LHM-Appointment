    private void jSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSubmitButtonActionPerformed
        
        //First name validation
        if ("".equals(jFName.getText())) 
        {
            jFName.setForeground(Color.red);
            jFName.setText("Please enter your First Name !");
        }
        
        //Last name validation
        if ("".equals(jSName.getText())) 
        {
            jSName.setForeground(Color.red);
            jSName.setText("Please enter your Surname !");
        }
        
        //Age validation
        if ("".equals(jPatientAge.getText())) 
        {
            jPatientAge.setForeground(Color.red);
            jPatientAge.setText("Please enter your Age !");
            
        }        
        
        //List of hospital objects
        Object[] HospitalObjects = HospitalList.getSelectedValues();
        
        //List of Docotors objects
        Object[] DrNameObejects = DrList.getSelectedValues(); 
        
        
        String[] hobbyStrings = new String[HospitalObjects.length];
        String[] hobbyStrings1 = new String[DrNameObejects.length];
        
        System.arraycopy(HospitalObjects, 0, hobbyStrings, 0,
                HospitalObjects.length);
        
        System.arraycopy(DrNameObejects, 0, hobbyStrings1, 0,
                DrNameObejects.length);
        
        //constructor
        Patient c = new Patient(Integer.parseInt(jPatientAge.getText()),
                ((Genderlist.getSelectedIndex()==0) ? History.MALE : 0) + 
                ((Genderlist.getSelectedIndex()==1) ? History.FEMALE : 0) ,
                hobbyStrings, hobbyStrings1, jFName.getText(), jSName.getText());
        

        callWarehouse(c);

    }//GEN-LAST:event_jSubmitButtonActionPerformed

